func (a announcementHandler) GetList(w http.ResponseWriter, r *http.Request) {
	var result GetListResponse

	ctx := r.Context()
	var decoder = schema.NewDecoder()
	var in GetListRequest
	err := decoder.Decode(&in, r.URL.Query())
	if err != nil {
		logrus.Errorln(err)
		result = GetListResponse{
			Meta: &Meta{
				Message: err.Error(),
				Status:  http.StatusInternalServerError,
				Code:    constants.DefaultCustomErrorCode,
			},
		}
		utils.JSONResponse(w, http.StatusInternalServerError, &result)
		return
	}
	defer a.AdminActivityLogService.Create(ctx, r, time.Now(), "Announcement", "Get List", nil)

	paginationData := common.PaginationRequest{
		Limit:  in.GetLimit(),
		Page:   in.GetPage(),
		Search: in.GetSearch(),
	}
	data, errs := a.AnnouncementService.GetList(ctx, paginationData, appConstants.AppTypeAdmin, in.GetType())
	if errs != nil {
		utils.PrintError(*errs)
		result = GetListResponse{
			Meta: &Meta{
				Message: errs.Err.Error(),
				Status:  uint32(errs.HttpCode),
				Code:    errs.CustomCode,
			},
		}
		utils.JSONResponse(w, errs.HttpCode, &result)
		return
	}

	resultData := []*GetListResponseData{}
	for _, v := range data.Data {
		studyPrograms := []*GetListResponseDataStudyProgram{}
		for _, w := range v.StudyPrograms {
			studyPrograms = append(studyPrograms, &GetListResponseDataStudyProgram{
				StudyProgramId:   w.StudyProgramId,
				StudyProgramName: w.StudyProgramName,
			})
		}

		resultData = append(resultData, &GetListResponseData{
			Id:               v.Id,
			Type:             v.Type,
			Title:            v.Title,
			AnnouncementDate: utils.SafetyDate(v.AnnouncementDate),
			FileUrl:          v.FileUrl,
			FilePath:         utils.NullStringScan(v.FilePath),
			FilePathType:     utils.NullStringScan(v.FilePathType),
			FileTitle:        utils.NullStringScan(v.FileTitle),
			Content:          utils.NullStringScan(v.Content),
			ForLecturer:      v.ForLecturer,
			ForStudent:       v.ForStudent,
			StudyPrograms:    studyPrograms,
		})
	}

	result = GetListResponse{
		Meta: &Meta{
			Message: "Get List Announcement",
			Status:  http.StatusOK,
			Code:    constants.SuccessCode,
		},
		Pagination: &Pagination{
			Page:         uint32(data.Pagination.Page),
			Limit:        uint32(data.Pagination.Limit),
			Prev:         uint32(*data.Pagination.Prev),
			Next:         uint32(*data.Pagination.Next),
			TotalPages:   uint32(*data.Pagination.TotalPages),
			TotalRecords: uint32(*data.Pagination.TotalRecords),
		},
		Data: resultData,
	}
	utils.JSONResponse(w, http.StatusOK, &result)
}

func (a announcementHandler) Create(w http.ResponseWriter, r *http.Request) {
	var result CreateResponse

	ctx := r.Context()
	var in CreateRequest
	err := json.NewDecoder(r.Body).Decode(&in)
	if err != nil {
		logrus.Errorln(err)
		result = CreateResponse{
			Meta: &Meta{
				Message: err.Error(),
				Status:  http.StatusInternalServerError,
				Code:    constants.DefaultCustomErrorCode,
			},
		}
		utils.JSONResponse(w, http.StatusInternalServerError, &result)
		return
	}
	defer a.AdminActivityLogService.Create(ctx, r, time.Now(), "Announcement", "Create", &in)

	announcementDate, errs := utils.StringToTime(in.GetAnnouncementDate())
	if errs != nil {
		utils.PrintError(*errs)
		result = CreateResponse{
			Meta: &Meta{
				Message: errs.Err.Error(),
				Status:  uint32(errs.HttpCode),
				Code:    errs.CustomCode,
			},
		}
		utils.JSONResponse(w, errs.HttpCode, &result)
		return
	}
	data := objects.CreateAnnouncement{
		Type:             in.GetType(),
		Title:            in.GetTitle(),
		AnnouncementDate: announcementDate,
		FilePath:         in.GetFilePath(),
		FilePathType:     in.GetFilePathType(),
		FileTitle:        in.GetFileTitle(),
		Content:          in.GetContent(),
		ForLecturer:      in.GetForLecturer(),
		ForStudent:       in.GetForStudent(),
		StudyProgramIds:  in.GetStudyProgramIds(),
	}

	errs = a.AnnouncementService.Create(ctx, data)
	if errs != nil {
		utils.PrintError(*errs)
		result = CreateResponse{
			Meta: &Meta{
				Message: errs.Err.Error(),
				Status:  uint32(errs.HttpCode),
				Code:    errs.CustomCode,
			},
		}
		utils.JSONResponse(w, errs.HttpCode, &result)
		return
	}

	result = CreateResponse{
		Meta: &Meta{
			Message: "Create Announcement",
			Status:  http.StatusCreated,
			Code:    constants.SuccessCode,
		},
	}
	utils.JSONResponse(w, http.StatusCreated, &result)
}

