type officerActionHandler struct {
	*service.ServiceCtx
}

func (f officerActionHandler) GetList(w http.ResponseWriter, r *http.Request) {
	var result GetListResponse

	ctx := r.Context()
	var decoder = schema.NewDecoder()
	var in GetListRequest
	err := decoder.Decode(&in, r.URL.Query())
	if err != nil {
		logrus.Errorln(err)
		result = GetListResponse{
			Meta: &Meta{
				Message: err.Error(),
				Status:  http.StatusInternalServerError,
				Code:    constants.DefaultCustomErrorCode,
			},
		}
		utils.JSONResponse(w, http.StatusInternalServerError, &result)
		return
	}
	defer f.AdminActivityLogService.Create(ctx, r, time.Now(), "Officer Action", "Get List", nil)

	paginationData := common.PaginationRequest{
		Limit:  in.GetLimit(),
		Page:   in.GetPage(),
		Search: in.GetSearch(),
	}
	data, errs := f.OfficerActionService.GetList(ctx, paginationData)
	if errs != nil {
		utils.PrintError(*errs)
		result = GetListResponse{
			Meta: &Meta{
				Message: errs.Err.Error(),
				Status:  uint32(errs.HttpCode),
				Code:    errs.CustomCode,
			},
		}
		utils.JSONResponse(w, errs.HttpCode, &result)
		return
	}

	resultData := []*GetListResponseData{}
	for _, v := range data.Data {
		resultData = append(resultData, &GetListResponseData{
			Id:                          v.Id,
			DocumentTypeId:              v.DocumentTypeId,
			DocumentTypeName:            v.DocumentTypeName,
			DocumentActionId:            v.DocumentActionId,
			DocumentActionAction:        v.DocumentActionAction,
			DocumentActionEnglishAction: v.DocumentActionEnglishAction,
			OfficerId:                   v.OfficerId,
			OfficerName:                 v.OfficerName,
			OfficerTitle:                utils.NullStringScan(v.OfficerTitle),
			OfficerEnglishTitle:         utils.NullStringScan(v.OfficerEnglishTitle),
			OfficerStudyProgramId:       utils.NullStringScan(v.OfficerStudyProgramId),
			OfficerStudyProgramName:     utils.NullStringScan(v.OfficerStudyProgramName),
		})
	}

	result = GetListResponse{
		Meta: &Meta{
			Message: "Get List Officer Action",
			Status:  http.StatusOK,
			Code:    constants.SuccessCode,
		},
		Pagination: &Pagination{
			Page:         uint32(data.Pagination.Page),
			Limit:        uint32(data.Pagination.Limit),
			Prev:         uint32(*data.Pagination.Prev),
			Next:         uint32(*data.Pagination.Next),
			TotalPages:   uint32(*data.Pagination.TotalPages),
			TotalRecords: uint32(*data.Pagination.TotalRecords),
		},
		Data: resultData,
	}
	utils.JSONResponse(w, http.StatusOK, &result)
}

